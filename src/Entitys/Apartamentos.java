package Entitys;

public class Apartamentos {
	private int Numero;
	private int QtdQuartos;
	
	public int getNumero() {
		return Numero;
	}
	public void setNumero(int numero) {
		Numero = numero;
	}
	public int getQtdQuartos() {
		return QtdQuartos;
	}
	public void setQtdQuartos(int qtdQuartos) {
		QtdQuartos = qtdQuartos;
	}
}
