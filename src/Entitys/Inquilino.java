package Entitys;

public class Inquilino {
	private String nome;
	private String telefone;
	private int ApAlocado;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public int getApAlocado() {
		return ApAlocado;
	}
	public void setApAlocado(int apAlocado) {
		ApAlocado = apAlocado;
	}
}
