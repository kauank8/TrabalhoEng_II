package Entitys;

public class Proprietario {
	public String nome;
	public String telefone;
	public int ApComprado;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public int getApComprado() {
		return ApComprado;
	}
	public void setApComprado(int apComprado) {
		ApComprado = apComprado;
	}
	
	
	
}
